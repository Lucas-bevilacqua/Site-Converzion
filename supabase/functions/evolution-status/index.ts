import { serve } from "https://deno.land/std@0.168.0/http/server.ts"
import { createClient } from "https://esm.sh/@supabase/supabase-js@2"

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
}

serve(async (req) => {
  console.log('üì• Nova requisi√ß√£o recebida')
  
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    console.log('üëâ Requisi√ß√£o OPTIONS - retornando headers CORS')
    return new Response('ok', { headers: corsHeaders })
  }

  try {
    const { email } = await req.json()
    
    if (!email) {
      console.error('‚ùå Email n√£o fornecido')
      return new Response(
        JSON.stringify({ error: 'Email n√£o fornecido' }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 400 }
      )
    }

    console.log('üìß Verificando status para email:', email)

    // Get Supabase client
    const supabaseUrl = Deno.env.get('SUPABASE_URL')
    const supabaseKey = Deno.env.get('SUPABASE_SERVICE_ROLE_KEY')
    
    if (!supabaseUrl || !supabaseKey) {
      console.error('‚ùå Vari√°veis de ambiente do Supabase n√£o encontradas')
      return new Response(
        JSON.stringify({ error: 'Configura√ß√£o do servidor incompleta' }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 500 }
      )
    }

    const supabaseClient = createClient(supabaseUrl, supabaseKey)

    // Get empresa data
    console.log('üîç Buscando dados da empresa')
    const { data: empresa, error: empresaError } = await supabaseClient
      .from('Empresas')
      .select('url_instance, instance_name, apikeyevo')
      .eq('emailempresa', email)
      .single()

    if (empresaError || !empresa) {
      console.error('‚ùå Erro ao buscar empresa:', empresaError)
      return new Response(
        JSON.stringify({ error: 'Empresa n√£o encontrada' }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 404 }
      )
    }

    if (!empresa.url_instance || !empresa.apikeyevo || !empresa.instance_name) {
      console.error('‚ùå Credenciais da Evolution incompletas')
      return new Response(
        JSON.stringify({ 
          error: 'Credenciais do Evolution n√£o configuradas',
          needsSetup: true 
        }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 400 }
      )
    }

    // Clean up the URL and instance name
    const baseUrl = empresa.url_instance.split('/message')[0].replace(/\/+$/, '')
    // Remove espa√ßos e caracteres especiais do nome da inst√¢ncia
    const instanceName = encodeURIComponent(empresa.instance_name.trim())
    console.log('üåê URL base da inst√¢ncia:', baseUrl)
    console.log('üì± Nome da inst√¢ncia (encoded):', instanceName)

    try {
      // Primeiro verifica se existem inst√¢ncias
      console.log('üì± Verificando inst√¢ncias existentes')
      const fetchInstancesResponse = await fetch(`${baseUrl}/instance/fetchInstances`, {
        method: 'GET',
        headers: {
          'Content-Type': 'application/json',
          'apikey': empresa.apikeyevo
        }
      })

      if (!fetchInstancesResponse.ok) {
        console.error('‚ùå Erro ao buscar inst√¢ncias:', await fetchInstancesResponse.text())
        return new Response(
          JSON.stringify({ error: 'Erro ao verificar inst√¢ncias', needsSetup: true }),
          { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 404 }
        )
      }

      // Verifica status da conex√£o
      console.log('üì± Verificando status da conex√£o:', instanceName)
      const statusResponse = await fetch(`${baseUrl}/instance/connectionState/${instanceName}`, {
        method: 'GET',
        headers: {
          'Content-Type': 'application/json',
          'apikey': empresa.apikeyevo
        }
      })

      if (!statusResponse.ok) {
        const errorText = await statusResponse.text()
        console.error('‚ùå Erro na resposta do Evolution:', errorText)
        
        if (statusResponse.status === 404) {
          return new Response(
            JSON.stringify({ error: 'Inst√¢ncia n√£o encontrada', needsSetup: true }),
            { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 404 }
          )
        }
        
        throw new Error(`Evolution API returned ${statusResponse.status}: ${errorText}`)
      }

      const statusData = await statusResponse.json()
      console.log('‚úÖ Status data:', statusData)

      // Update connection status in database
      const isConnected = statusData.state === 'open'
      const { error: updateError } = await supabaseClient
        .from('Empresas')
        .update({ is_connected: isConnected })
        .eq('emailempresa', email)

      if (updateError) {
        console.error('‚ùå Erro ao atualizar status:', updateError)
      }

      return new Response(
        JSON.stringify({ 
          success: true, 
          isConnected,
          state: statusData.state,
          instanceExists: true
        }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' } }
      )

    } catch (error) {
      console.error('‚ùå Erro ao verificar status:', error)
      return new Response(
        JSON.stringify({ error: `Erro ao verificar status: ${error.message}` }),
        { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 500 }
      )
    }

  } catch (error) {
    console.error('‚ùå Erro na fun√ß√£o:', error)
    return new Response(
      JSON.stringify({ error: 'Erro interno do servidor' }),
      { headers: { ...corsHeaders, 'Content-Type': 'application/json' }, status: 500 }
    )
  }
})